///////////////////////////////////////////////////////////////////////////////
////////////////////////////////// Constants //////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

const COW_KING_SUPERUNIQUE_NAME = "The Cow King";

const SUPERUNIQUE_NAME_COLUMN  = "Name";
const SUPERUNIQUE_CLASS_COLUMN = "Class";

const COW_KING_D2R_CLASS    = "cowking";
const COW_KING_LEGACY_CLASS = "hellbovine";

const SUPERUNIQUES_FILENAME = 'global\\excel\\superuniques.txt';
const SUPERUNIQUES_FILE     = D2RMM.readTsv(SUPERUNIQUES_FILENAME);

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////// Main ////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

fixCowKingDrop();

///////////////////////////////////////////////////////////////////////////////
//////////////////////////// Superuniques Functions ///////////////////////////
///////////////////////////////////////////////////////////////////////////////

function isTheCowKing(superUniqueRow) {
	return superUniqueRow[SUPERUNIQUE_NAME_COLUMN] === COW_KING_SUPERUNIQUE_NAME;
}

function fixCowKingDrop() {
	
	const theCowKing = SUPERUNIQUES_FILE.rows
		.find((currSuperUniqueRow) => isTheCowKing(currSuperUniqueRow));

	theCowKing[SUPERUNIQUE_CLASS_COLUMN] = COW_KING_LEGACY_CLASS;
	
	D2RMM.writeTsv(SUPERUNIQUES_FILENAME, SUPERUNIQUES_FILE);

}